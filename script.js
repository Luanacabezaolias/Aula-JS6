let opcao = parseInt(prompt("Dgite uma opção"));

switch(opcao){
    case 1:
        document.write("Primeira opção")
    break;
    case 2:
        document.write("Segunda opção")
    break;
    case 3:
        document.write("Terceira opção")
    break;
        default:
            document.write("Nenhuma das opções válidas")
        break;


}